#include <iostream>
#include <iomanip>
#include <cmath>

using namespace std;
/****************************************************************/
/*             О С Н О В Н А Я     П Р О Г Р А М М А            */
/****************************************************************/
int main()
{
    // Объявление переменных
    double A = 0;                // начальная точка отрезка
    double B = 0;                // конечная точка отрезка
    int N = 0;                   // число интервалов разбиения
    double h = 0;                // величина шага
    double x = 0, F = 0, G = 0;  // переменная x и значения функций F и G
    int i = 0;                   // счётчик итераций
    
    // Запрос исходных данных
    cout << "Введите начало интервала A: ";
    cin >> A;
    cout << endl;
    cout << "Введите конец интервала B: ";
    cin >> B;
    cout << endl;
    
    // Проверка корректности границ интервала
    while (A >= B)
    {
        cout << "Ошибка! Начало интервала должно быть меньше конца." << endl;
        cout << "Пожалуйста, введите корректные значения, где A < B." << endl;
        cout << "Введите начало интервала A: ";
        cin >> A;
        cout << endl;
        cout << "Введите конец интервала B: ";
        cin >> B;
    }
    cout << endl;
    
    cout << "Введите количество интервалов N: ";
    cin >> N;
    
    // Проверка корректности количества интервалов
    while (N <= 0)
    {
        cout << "Ошибка! Количество интервалов должно быть положительным числом." << endl;
        cout << "Пожалуйста, введите значение N > 0." << endl;
        cout << "Введите количество интервалов N: ";
        cin >> N;
    }
    
    cout << endl;
    cout << endl;
    cout << endl;
    cout << endl;
    
    // Отображение введённых параметров
    cout << "Введённые параметры:" << endl;
    cout << endl;
    cout << "A = " << A << endl
         << "B = " << B << endl
         << "N = " << N << endl;
    cout << endl;
    
    // Расчёт шага табуляции
    h = (B - A) / N;
    
    // Формирование заголовка таблицы
    cout << "┌───────┬───────────┬───────────┬───────────┐" << endl;
    cout << "│   i   │     x     │    F(x)   │    G(x)   │" << endl;
    cout << "├───────┼───────────┼───────────┼───────────┤" << endl;
    
    // Установка формата вывода чисел
    cout << fixed << setprecision(3);
    
    // Цикл вычисления и вывода значений функций
    while(i <= N)
    {
        x = A + i * h;     // вычисление текущего значения аргумента
        F = pow((x - 1), 3);    // вычисление F(x) = (x-1)^3
        G = pow((x + 5), 3) / (1 + pow(sin(x), 2));   // вычисление G(x)
        
        // Вывод строки таблицы с выровненными значениями
        cout << "│ " << setw(5) << i << " │ "
             << setw(9) << x << " │ "
             << setw(9) << F << " │ "
             << setw(9) << G << " │" << endl;
        i++;
    }
    
    // Завершение таблицы
    cout << "└───────┴───────────┴───────────┴───────────┘" << endl;
    
    return 0;
}
